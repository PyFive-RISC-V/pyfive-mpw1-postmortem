#define IOCONF_BASE	0x26000000
#define IOCONF_OFS(x)	(0x20 + 4 * (x))(a0)

	.section .text.start
	.global _start
_start:
	// Set configuration
	li a0, IOCONF_BASE

	// Clear all registers
	li a1, 0x26000020
	li a2, 0x260000b8

1:
	sw zero, 0(a1)
	addi    a1, a1, 4
	bne	a1, a2, 1b

	// Auto ---
	li s3, 0x0020
	sw s3, IOCONF_OFS(16)
	li s3, 0x0013
	sw s3, IOCONF_OFS(17)

	// Trigger load
	li s0, 1
	sw s0, 0(a0)

1:
	lw s0, 0(a0)
	bne s0, zero, 1b

	// Midi
	li a0, 0x30100000

	li s0, 199
	sw s0, 4(a0)	// DIV = 202 -> 31250 baud

	// Program setup
	li s0, 192
	sw s0, 0(a0)

	li s0, 30
	sw s0, 0(a0)

	li s0, 193
	sw s0, 0(a0)

	li s0, 29
	sw s0, 0(a0)

	li s0, 194
	sw s0, 0(a0)

	li s0, 34
	sw s0, 0(a0)

	li s0, 201
	sw s0, 0(a0)

	li s0, 0
	sw s0, 0(a0)

	// Process messages
	la a1, midi_data

1:
	nop

		// Load delay
	lb s0, 0(a1)

		// Execute delay
2:
	nop
	beq s0, zero, 4f	// Exit ?


	li s4, 80		// Inner delay loop
3:
	nop
	addi s4, s4, -1
	bne s4, zero, 3b

	addi s0, s0, -1
	j 2b

4:
		// Send message
	lb s1, 1(a1)
	sw s1, 0(a0)
	lb s1, 2(a1)
	sw s1, 0(a0)
	lb s1, 3(a1)
	sw s1, 0(a0)

		// Loop
	addi a1, a1, 4
	j 1b


